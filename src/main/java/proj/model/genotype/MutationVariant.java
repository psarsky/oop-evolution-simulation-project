package proj.model.genotype;

/**
 * Enum representing the different types of mutation strategies
 * that can be applied to an animal's genotype.
 */
public enum MutationVariant {
    /**
     * RANDOM:
     *
     * A mutation strategy where genes are altered in a completely random manner.
     * The specific implementation determines which genes are mutated and how.
     */
    RANDOM
}